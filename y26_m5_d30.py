p = "   hello world   "
P = "   HELLO WORLD   "

print(p.strip()) #strip() method removes any leading (spaces at the beginning) and trailing (spaces at the end) characters (space is the default leading character to remove)
print(p)
print(P.lstrip()) #lstrip() method removes any leading (spaces at the beginning) characters (space is the default leading character to remove)
print(P.rstrip()) #rstrip() method removes any trailing (spaces at the end) characters (space is the default trailing character to remove)
